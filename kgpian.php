Eligibility
Any person who has received a degree from the Institute is eligible to be considered for Distinguished Alumnus Award, except in the following cases:
A.    An alumnus/alumna, who is an employee of IIT Kharagpur, shall not be eligible for the award during his/her service period except when he/she has accomplished with truly outstanding of highest order recognized widely. 
B.    An alumnus/alumna who has received the Life Fellow Award or the D. Sc. (Honoris Causa) shall not be considered for Distinguished Alumnus Award within a period of 5 years.
 
Criteria:
An individual, to be nominated for this Distinguished Alumnus Award, should have made significant professional contribution which is recognized widely. The following, among possible other criteria, shall be considered while assessing the nominations:
•    Positions held 
•    Contributions to his/her profession
•    Awards and Honours Received 
•    Other contributions
 
Process for Selection
Nominations shall be short listed by a Screening Committee (SC) chaired by the Dean (AA&IR) to be constituted with the approval of the Director. Short listed nominations shall be peer reviewed. The SC will make a panel of such reviewers for the purpose. Based on the recommendations of the peer reviewers, the SC will make a panel for consideration of the Standing Committee. The Standing Committee will normally comprise Director as the Chairman, Deputy Director, Dean (AA&IR), Dean (F&P) as members and 2 (two) External Experts, following the guidelines of selection in such cases and considering recommendations of the peer reviewers making final recommendations to the Senate and the Board of Governors.
